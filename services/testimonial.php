   <h2 class='headline' >My Path Towards Bitcoin</h2>
   <br><br>


<h5> <strong>Posted 4/5/21 by Stephanie Sprague</strong> </h5>
   <br><br>   

<p> I wish I could say my decision to invest in Bitcoin was based on some cool fact or a piece of infor­ma­tion I discov­ered. Neither was the case. No person or spread sheet was involved either. I wasn’t in investor mode in the final weeks of 2020. In fact, I was in the opposite state of mind. After long days reporting on the US presidential election for a public service broad­caster, I went into recovery mode. It was by switching off that I came to the realiza­tion that digital curren­cies had arrived. It would be irrespon­sible not to buy Bitcoin. 
</p>
<p>
I didn’t even know the price of Bitcoin. I hadn’t even heard of Michael Saylor. Within days, by coinci­dence, I met a HODLer who steered me in the right direc­tion (Swan Bitcoin). I bemoaned my regrets about not getting involved even a few months earlier when Bitcoin was at $9,000. “Anything below $100,000 is a bargain,” he said. When-the-student-is-ready-the-teacher arrives is how I view Bitcoin. You have to accept where you are on the journey. 
</p>
<br> <br>
<h2 class='headline'> Anything below $100,000 is a bargain </h2>
<br> <br>
<h4><stong><em>- Bitcoin HODLer</em></strong></h4>
   <br>
<p>
There’s an insatiable need to know more and to be certain in the Information Age.  I hear a lot of people say they don’t know enough to buy Bitcoin. With this kind of thinking no one would’ve ever shared personal details on Facebook or banked electronically. Is it neces­sary to under­stand the complexity of Amazon before you buy something in less than a minute? The easier the end-user can inter­face with software and technology, the more complex the backend. Do you know what happens when you use your debit card to buy groceries? It might take days for that trans­ac­tion to process. How are checks processed? I got a crash-course in this after someone created fake checks using my routing and account numbers. My decision about Bitcoin wasn’t based on a deep dive under the hood, but on my experi­ence as a finan­cial reporter for thirty years. 
</p>

<p>
Finan­cial news didn’t feel like a great move when I began my career in the early ‘90s. Until then business news was largely relegated to “Wall Street Week in Review.” But little did I antic­i­pate the Finan­cial Armageddon stories. Who needs to cover war zones when you were on a Chicago trading floor during a panic? Bitcoin was born after the 2007 – 2008 credit crisis and Lehman blow-up, but seeded long before – I reckon sometime after Black Wednesday and before Long-Term Capital Manage­ment in what I call The Deriv­a­tives Debacles. The Deriv­a­tives Debacles have been overlooked in the annals of Armageddon history. But this was when states and munic­i­pal­i­ties, entrusted to respon­sibly invest public funds, lost money after specu­lating in futures, options and swaps. The numbers pale in compar­ison with the 2001 DotCom bust or the 2007 – 2008 invest­ment banking subprime fiasco. But they were a turning point: They ushered in the era of the Greenspan put, “Too Big To Fail”, and destruc­tive excesses whereby each ensuing crisis would extract a bigger toll on society. 
</p>
<p>
As I covered these stories, I started to see the fragili­ties of the fiat monetary system. This affected me person­ally. While I was reporting on the god-like utter­ances of Greenspan, I was also buying gold. I started buying below $400 an ounce and stopped at the turn of the Millen­nium when I found it a nuisance to store. I also found gold deeply unful­filling to keep under lock and key. It feels like a waste to keep beauty in the dark, out of sight, and useless, like the troll counting coins under the bridge. Gold is a conductor of electricity and heat after all. There might be reasons besides aesthetics that gold adorns cathe­drals. There might be reasons for Donald Trump to bedeck his NYC penthouse in gold. I’ve since limited buying precious metals; I joke that metals are for ingesting – colloidal – or wearing – jewelry. 
</p>
<p>
The trends I saw in the last 30 years climaxed during the Covid-19 lockdowns. One pundit described them as “a controlled demoli­tion of the economy,” It’s a controlled demoli­tion of society, too. Re-opening economies — once Joe Biden became Presi­dent – isn’t happening with a snap of the fingers. Too many competing inter­ests have an agenda in enlarging the Bio-Security or Bio-Polit­ical State. Too many civil­ians have adapted quite well too. Some don’t want to go back to the real world; some don’t want to work outside the virtual. Just ask several mayors and school board super­in­ten­dents demanding teachers get back into class­rooms for full-time in-person instruc­tion. Speaking of which: I hear the adage “kids adapt so well.” Yes, but what do our leaders want them to adapt to? I’ve seen the toll on kids and there are some who won’t recover. There are some lost from ever integrating into society, let alone supporting its struc­tures. The physical and mental devel­op­mental costs to our children will be studied in years to come. Will future spending address these deeper long-term needs, or prime kids for a life on UBI?
</p>

<p>
When Presi­dent Trump announced the CARES Act I assumed it would be a one-off, not possibly a new way of life. More than $3 trillion in spending in 2020 and another $2 trillion in Q1 2021 may have met short-term survival needs – but also indulged luxury tastes. I met a Grubhub driver from Kalamazoo buying a Montcler coat on Oak Street in Chicago. I met minimum wage workers buying bling at Tiffany’s. On Biden’s Inaugu­ra­tion Day, I heard comments like “mighty fine party there — now where’s my stimmy?” The checks have barely gone out and word’s out the White House is preparing another $3 trillion in spending.
</p>
<p>
The govern­ment largesse is being treated as a carrot, but it can be used as a stick too. It was no accident that Bitcoin rallied above $40,000 on the day Biden announced his $1.9 trillion spending plan, upping the handouts to $1,400 per couple. Bitcoin prices have been tracking funda­mental factors, rising on “the waves”: there’s talk of a fourth Covid-19 wave and the Biden Democ­ratic Wave. It was no accident either Bitcoin climbed above $60,000 after Biden’s prime-time TV address warning about what people “can and cannot do once fully vacci­nated.” Welcome to the Bio-Polit­ical State. Instead of vacci­na­tions providing a passport to freedom, expect yet more restric­tions on individual liber­ties and on the economy.
</p>

<p>
In retro­spect I wasn’t the only one desperate to buy Bitcoin below $20,000. And the longer businesses, stores and restau­rants are restricted in the G7 and Common­wealth countries, the more this will disrupt the global scale of produc­tion for food, drink, clothing, and textiles that has allowed these goods to be relatively cheap. US CPI data and anecdotal evidence show prices for basic goods like food contin­uing to rise. 
</p>
<p>
A few months back, ECB Presi­dent Chris­tine Lagarde had an honest moment. Calling for global regula­tion of Bitcoin, she said “if there is an escape, an escape could be used.” She didn’t say why a lawful person might need such an escape, and what we’ll need to escape from. But with Bitcoin I feel a little less power­less about whatever she has in mind.
</p>

   
